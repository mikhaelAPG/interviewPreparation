public class PrintNumbers {
    public static void main(String[] args) {
        // Inisialisasi variabel number dengan nilai 1
        int number = 1;

        // Loop selama number kurang dari atau sama dengan 10
        while (number <= 10) {
            // Mencetak angka ke konsol
            System.out.println(number);
            // Menambahkan 1 ke number pada setiap iterasi
            number++;
        }
    }
}
